export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {//下面这些是在实现什么
  // 这段代码定义了一个名为 RootLayout 的 React 组件，它接受一个 props 对象，其中包含一个 children 属性，类型为 React.ReactNode。这个组件是一个布局组件，用于包裹应用程序中的其他组件和页面。通过使用 RootLayout 组件，可以在整个应用程序中保持一致的布局和样式。在这个组件中，children 属性表示被包裹的子组件或页面内容，这些内容将被渲染在布局中。
  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        {children}
      </body>
    </html>
  );
}
